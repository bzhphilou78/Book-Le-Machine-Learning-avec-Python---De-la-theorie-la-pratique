import math

pi=math.pi

print(f"round({pi}) = {round(pi)}")
print(f"round({pi},2) = {round(pi,2)}")
print(f"trunc({pi}) = {math.trunc(pi)}")
print(f"ciel({pi}) = {math.ceil(pi)}")
print(f"floor({pi}) = {math.floor(pi)}")

print(f"la racine carrée de 25 est égale à : {math.sqrt(25)}")
print(f"La valeur de e est : {math.e} et ln(e) = {math.log(math.e)}")

 